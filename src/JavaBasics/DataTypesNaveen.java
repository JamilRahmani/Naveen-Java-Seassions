package JavaBasics;

public class DataTypesNaveen {

	public static void main(String[] args) {

		// primitive data type int, double, char, boolean
		
		// 1. int:
		int i = 10;
		int i1 = 20;
		int i2 = -2;
		System.out.println(i+i1+i2 );
		
		// 2. double:
		double d = 12.34;
		double d1 = 34.44;
		System.out.println(d1-d);
		
		// 3. char: only single digit value, should be written within single quotes
		char c = 'a';
		char c1 = '1';
		char c2 = '$';
		
		//4. boolean
		boolean b1 = true;
		boolean  b2 = false;
		
		// 5. String: is a class, not a data type
		String s =  "Hello";
		String s1 = "Selenium";
		String s2 = "This is my first java code";
		String s3 = "1000";
		String s4 = "12.33";
		
		System.out.println(s2);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
